package br.com.esigtm.model;

public class Prioridade {

    private String descricao;
    private String id;

    public Prioridade() {
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}