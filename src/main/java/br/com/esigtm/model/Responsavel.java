package br.com.esigtm.model;

public class Responsavel {

    private String nome;
    private String id;

    public Responsavel() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
